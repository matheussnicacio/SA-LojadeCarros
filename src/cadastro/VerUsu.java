            DefaultTableModel model =(DefaultTableModel) tbusu.getModel();
   model.setNumRows(0);
 
   while(rs.next())
   {
       model.addRow(new Object[]
       {
          //retorna os dados da tabela do BD, cada campo e um coluna.
          rs.getString("id_user"),
          rs.getString("nm_user"),
          rs.getString("ds_login")
       });
  }
